/* USER CODE BEGIN 4 */
void Calculate_ADC(void)
{

    // Task 1
    adc_sum[0]=0; adc_sum[1]=0; adc_sum[2]=0; adc_sum[3]=0;
    for (int i = 0; i < 40; i++) adc_sum[i % 4] += adc_buffer[i];

    val_M = ((adc_sum[0] * 2000) / 10) / 4095;
    val_N = ((adc_sum[1] * 500) / 10) / 4095;
    val_TempSet = 20 + (((adc_sum[2] * 20) / 10) / 4095);
    internal_temp = __HAL_ADC_CALC_TEMPERATURE(3300, adc_sum[3]/10, ADC_RESOLUTION_12B);

    // =========================================================
    // Logic Pulse Sequence (Non-blocking using HAL_GetTick)
    // =========================================================

    // Check State 1: กำลังรอ 100ms อยู่หรือเปล่า?
    if (pulse_state == 1)
    {
        if (HAL_GetTick() - start_tick >= 100)
        {
            // after 100ms ดีดขึ้น High (เริ่ม Pulse N)
            HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET);

            // สั่ง TIM2 ทำงานต่อ (us)
            __HAL_TIM_SET_AUTORELOAD(&htim2, val_N + 100 - 1);
            __HAL_TIM_SET_COUNTER(&htim2, 0);
            HAL_TIM_Base_Start_IT(&htim2);

            pulse_state = 2;
        }
        return;
    }

    // Check Trigger
    if ((abs((int)val_N - (int)last_N) > 5 || abs((int)val_M - (int)last_M) > 5) && pulse_state == 0)
    {
        last_N = val_N;
        last_M = val_M;

        // Low
        HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);

        // 100ms
        start_tick = HAL_GetTick();

        pulse_state = 1;
    }
}

// TIM2 Interrupt
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM2)
    {
        switch (pulse_state)
        {
            case 2: // จบ Pulse N (High) -> ลง Low (Pulse M)
                HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);
                __HAL_TIM_SET_AUTORELOAD(&htim2, val_M + 100 - 1);
                pulse_state = 3;
                break;

            case 3: // จบ Pulse M (Low) -> กลับไป High (Idle)
                HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET);
                HAL_TIM_Base_Stop_IT(&htim2);
                pulse_state = 0;
                break;
        }
    }
}
